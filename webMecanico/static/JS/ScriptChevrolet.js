/* Ventanilla 1 Marca Chevrolet */
const abrir1 = document.querySelector("#imgChevrolet1");
const model1 = document.querySelector("#model7");
const cerrar1 = document.querySelector("#btnCerrar1");

const abrirVentana1 = () => {
    model1.showModal();
}

const cerrarVentana1 = () => {
    model1.close();
}

abrir1.addEventListener("click", abrirVentana1)

cerrar1.addEventListener("click", cerrarVentana1)

